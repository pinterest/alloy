/** @jsxImportSource @alloy-js/core */
import { code } from "@alloy-js/core";
import { d } from "@alloy-js/core/testing";
import { describe, it } from "vitest";
import * as gql from "../src/index.js";
import { assertFileContents, toGraphQLTextMultiple } from "./utils.js";

describe("SourceFile", () => {
  it("renders an empty source file", () => {
    const content = <gql.SourceFile path="schema.graphql" />;
    const res = toGraphQLTextMultiple([content]);

    assertFileContents(res, {
      "schema.graphql": d`
      
      `,
    });
  });

  it("renders a source file with types", () => {
    const content = (
      <gql.SourceFile path="schema.graphql">
        <gql.ObjectTypeDefinition name="User">
          <gql.FieldDefinition
            name="id"
            type={code`${gql.builtInScalars.ID}!`}
          />
          <gql.FieldDefinition
            name="name"
            type={code`${gql.builtInScalars.String}!`}
          />
          <gql.FieldDefinition name="email" type={gql.builtInScalars.String} />
        </gql.ObjectTypeDefinition>
        <gql.ObjectTypeDefinition name="Post">
          <gql.FieldDefinition
            name="id"
            type={code`${gql.builtInScalars.ID}!`}
          />
          <gql.FieldDefinition
            name="title"
            type={code`${gql.builtInScalars.String}!`}
          />
          <gql.FieldDefinition
            name="content"
            type={gql.builtInScalars.String}
          />
        </gql.ObjectTypeDefinition>
      </gql.SourceFile>
    );

    const res = toGraphQLTextMultiple([content], {});

    assertFileContents(res, {
      "schema.graphql": d`
        type User {
          id: ID!
          name: String!
          email: String
        }
        
        type Post {
          id: ID!
          title: String!
          content: String
        }
      `,
    });
  });

  it("renders a source file with header comment", () => {
    const content = (
      <gql.SourceFile
        path="schema.graphql"
        headerComment="Auto-generated schema"
      >
        <gql.ObjectTypeDefinition name="User">
          <gql.FieldDefinition
            name="id"
            type={code`${gql.builtInScalars.ID}!`}
          />
        </gql.ObjectTypeDefinition>
      </gql.SourceFile>
    );

    const res = toGraphQLTextMultiple([content], {});

    assertFileContents(res, {
      "schema.graphql": d`
        # Auto-generated schema
        type User {
          id: ID!
        }
      `,
    });
  });

  it("renders a source file with documentation", () => {
    const content = (
      <gql.SourceFile
        path="schema.graphql"
        description="Main GraphQL schema for the application"
      >
        <gql.ObjectTypeDefinition name="User">
          <gql.FieldDefinition
            name="id"
            type={code`${gql.builtInScalars.ID}!`}
          />
        </gql.ObjectTypeDefinition>
      </gql.SourceFile>
    );

    const res = toGraphQLTextMultiple([content], {});

    assertFileContents(res, {
      "schema.graphql": d`
        """
        Main GraphQL schema for the application
        """
        type User {
          id: ID!
        }
      `,
    });
  });

  it("renders a source file with header and documentation", () => {
    const content = (
      <gql.SourceFile
        path="schema.graphql"
        headerComment="Generated by Alloy"
        description="User management schema"
      >
        <gql.ObjectTypeDefinition name="User">
          <gql.FieldDefinition
            name="id"
            type={code`${gql.builtInScalars.ID}!`}
          />
          <gql.FieldDefinition
            name="name"
            type={code`${gql.builtInScalars.String}!`}
          />
        </gql.ObjectTypeDefinition>
      </gql.SourceFile>
    );

    const res = toGraphQLTextMultiple([content], {});

    assertFileContents(res, {
      "schema.graphql": d`
        # Generated by Alloy
        """
        User management schema
        """
        type User {
          id: ID!
          name: String!
        }
      `,
    });
  });

  it("renders multiple source files in a schema", () => {
    const content = [
      <gql.SourceFile path="user.graphql">
        <gql.ObjectTypeDefinition name="User">
          <gql.FieldDefinition
            name="id"
            type={code`${gql.builtInScalars.ID}!`}
          />
          <gql.FieldDefinition
            name="name"
            type={code`${gql.builtInScalars.String}!`}
          />
        </gql.ObjectTypeDefinition>
      </gql.SourceFile>,
      <gql.SourceFile path="post.graphql">
        <gql.ObjectTypeDefinition name="Post">
          <gql.FieldDefinition
            name="id"
            type={code`${gql.builtInScalars.ID}!`}
          />
          <gql.FieldDefinition
            name="title"
            type={code`${gql.builtInScalars.String}!`}
          />
        </gql.ObjectTypeDefinition>
      </gql.SourceFile>,
    ];

    const res = toGraphQLTextMultiple(content);

    assertFileContents(res, {
      "user.graphql": d`
        type User {
          id: ID!
          name: String!
        }
      `,
      "post.graphql": d`
        type Post {
          id: ID!
          title: String!
        }
      `,
    });
  });

  it("renders a source file without content correctly", () => {
    const content = <gql.SourceFile path="empty.graphql" />;

    const res = toGraphQLTextMultiple([content]);

    assertFileContents(res, {
      "empty.graphql": d`
      
      `,
    });
  });
});
